// ================= IFC Data Transformation =================

function getTgvIfcData(modelId) {
    const data = tgvData.models[modelId];
    if (!data) return null;

    const realModelId = modelId.replace("_3PH", "");

    return [
        { group: "Identity", property: "IfcEntityType", value: "IfcUnitaryControlElement" },
        { group: "Identity", property: "IfcTag", value: realModelId },
        { group: "Identity", property: "ObjectType", value: data.Type },
        { group: "Pset_ManufacturerTypeInformation", property: "Manufacturer", value: data.Brand },
        { group: "Pset_ManufacturerTypeInformation", property: "ModelReference", value: realModelId },
        { group: "Pset_AirConditioningUnit", property: "NominalCoolingCapacity", value: data.NominalCoolingCapacity + " BTU/hr" },
        { group: "Pset_AirConditioningUnit", property: "RefrigerantType", value: data.RefrigerantType },
        { group: "Pset_ElectricalDeviceCommon", property: "NominalVoltage", value: data.NominalVoltage + " V" },
        { group: "Pset_ElectricalDeviceCommon", property: "NumberofPhases", value: data.NumberofPhases },
        { group: "Pset_ElectricalDeviceCommon", property: "NominalFrequency", value: data.NominalFrequency + " Hz" },
        { group: "Pset_Carrier_Custom_Specs", property: "Outdoor_Model", value: data.Outdoor_Model },
        { group: "Pset_Carrier_Custom_Specs", property: "SEER", value: data.SEER },
        { group: "Pset_Carrier_Custom_Specs", property: "PowerConsumption", value: data.PowerConsumption + " kW" },
        { group: "Pset_Carrier_Custom_Specs", property: "OperatingCurrent", value: data.OperatingCurrent + " A" },
        { group: "Pset_Carrier_Custom_Specs", property: "Indoor_Dimensions_mm", value: `${data.Indoor_Height_mm} x ${data.Indoor_Width_mm} x ${data.Indoor_Depth_mm}` },
        { group: "Pset_Carrier_Custom_Specs", property: "Outdoor_Dimensions_mm", value: `${data.Outdoor_Height_mm} x ${data.Outdoor_Width_mm} x ${data.Outdoor_Depth_mm}` },
        { group: "Pset_Carrier_Custom_Specs", property: "Weight_Indoor_kg", value: data.Weight_Indoor_kg },
        { group: "Pset_Carrier_Custom_Specs", property: "Weight_Outdoor_kg", value: data.Weight_Outdoor_kg },
        { group: "Pset_Carrier_Custom_Specs", property: "Pipe_Liquid_inch", value: data.Pipe_Liquid },
        { group: "Pset_Carrier_Custom_Specs", property: "Pipe_Suction_inch", value: data.Pipe_Suction },
        { group: "Pset_Carrier_Custom_Specs", property: "CompressorType", value: data.CompressorType }
    ];
}

function getTgvModelList() {
    return Object.keys(tgvData.models).sort();
}
